package org.opensplice.demo;

public final class ColorRGB {

    public short red;
    public short green;
    public short blue;

    public ColorRGB() {
    }

    public ColorRGB(
        short _red,
        short _green,
        short _blue)
    {
        red = _red;
        green = _green;
        blue = _blue;
    }

}
