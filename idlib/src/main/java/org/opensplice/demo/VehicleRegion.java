package org.opensplice.demo;

public final class VehicleRegion {

    public int vid;
    public int x;
    public int y;
    public int width;
    public int height;

    public VehicleRegion() {
    }

    public VehicleRegion(
        int _vid,
        int _x,
        int _y,
        int _width,
        int _height)
    {
        vid = _vid;
        x = _x;
        y = _y;
        width = _width;
        height = _height;
    }

}
