package org.opensplice.demo;

public final class ShapeType {

    public java.lang.String color = "";
    public int x;
    public int y;
    public int shapesize;

    public ShapeType() {
    }

    public ShapeType(
        java.lang.String _color,
        int _x,
        int _y,
        int _shapesize)
    {
        color = _color;
        x = _x;
        y = _y;
        shapesize = _shapesize;
    }

}
